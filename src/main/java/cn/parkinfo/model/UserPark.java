package cn.parkinfo.model;

public class UserPark {

	private int id;
	private int userId;
	private int parkId;
	public int getId() {
		return id;
	}
	public int getUserId() {
		return userId;
	}
	public int getParkId() {
		return parkId;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public void setParkId(int parkId) {
		this.parkId = parkId;
	}
	
	
}
