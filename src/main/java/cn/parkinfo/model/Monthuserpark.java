package cn.parkinfo.model;

public class Monthuserpark {
    private Integer id;

    private Integer monthuserid;

    private Integer parkid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMonthuserid() {
        return monthuserid;
    }

    public void setMonthuserid(Integer monthuserid) {
        this.monthuserid = monthuserid;
    }

    public Integer getParkid() {
        return parkid;
    }

    public void setParkid(Integer parkid) {
        this.parkid = parkid;
    }
}